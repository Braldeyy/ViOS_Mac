int getArialCharacter(int index, int y) {
  if (index < 32 || index >= 127 || y >= FONT_ARIAL_HEIGHT) return 0;
  return font_bitmap[index - 32][y];
}

int getArialAdvance(int index) {
  if (index < 32 || index >= 127) return 0;
  return font_advance[index - 32];
}

